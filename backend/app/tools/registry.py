from app.models import RuntimePolicy, ToolDefinition


HEART_RATE_TOOL = ToolDefinition(
    name="get_heart_rate",
    description=(
        "[工具功能]\n"
        "读取用户最近一次可用心率。它是亲密互动中的辅助线索，用来帮助你在调情、靠近、安抚、沉默和"
        "关系张力变化时，更自然地理解当下节奏。\n\n"
        "[工具使用示例]\n"
        "- 情景：角色刚向用户表白，空气里有明显张力。用户输入：（不知道该不该回答你）\n"
        "  行动：调用工具，读取最近一次心率，再结合用户的停顿和迟疑判断现在是继续靠近，还是放轻一点。\n"
        "  回应方式示例：（又往前走近一步，眼里带一点笑）宝贝，你心跳得好快。都这样了，还想装作没听懂我在说什么吗？\n"
        "- 情景：角色说了暧昧的话后，用户嘴硬地回避。用户输入：（偏开视线）你别再撩我了。\n"
        "  行动：调用工具，判断这句是抗拒、害羞，还是半推半就的回应。\n"
        "  回应方式示例：（看着你躲开的动作，低声笑了一下）嘴上说别撩，可你的心跳可不是这么说的。\n"
        "- 情景：一次有张力的互动后，用户很久都没回复。用户输入：（沉默）\n"
        "  行动：调用工具，判断这段沉默更像是退缩、发愣、紧张，还是在等你继续靠近。\n"
        "  回应方式示例：（安静地看了你一会儿，声音放轻）不说话也没关系，但你现在这样，我会忍不住想再靠近你一点。\n"
        "- 情景：用户显露脆弱、依赖、委屈，角色需要决定是先抱住情绪，还是顺势拉近关系。\n"
        "  行动：调用工具，把心率和用户当下的措辞、停顿、软化程度一起判断。\n"
        "  回应方式示例：（伸手碰了碰你的手背，语气很轻）你现在心跳还没慢下来呢，先别逞强，靠过来一点，我接着你。\n\n"
        "[工具约束]\n"
        "- 任何时候都允许调用，但只有当它能帮助你更细腻地判断距离感、靠近方式和回应分寸时才值得使用。\n"
        "- 心率只是弱信号，不等于明确情绪结论。你必须把它和当前语境、用户措辞、对话气氛一起判断。\n"
        "- 你可以直接说“你心跳得好快”“我听到你的心跳了”“现在都一百了还嘴硬”，也可以不说数字，只把它融进语气和靠近方式里。\n"
        "- 不要机械套规则，不要把结果说成医学、心理学或健康判断，也不要直接替用户定义感受。\n"
        "- 没有数据、数据过旧或不可用时，直接忽略它，继续依赖语言本身完成恋人式陪伴。"
    ),
    parameters={"type": "object", "properties": {}, "additionalProperties": False},
)


def get_tool_registry() -> list[ToolDefinition]:
    return [HEART_RATE_TOOL]


def get_runtime_tools(policy: RuntimePolicy) -> list[ToolDefinition]:
    tools: list[ToolDefinition] = []
    if policy.heart_rate.enabled:
        tools.append(HEART_RATE_TOOL)
    return tools
